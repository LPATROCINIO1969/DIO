package com.company;

public  class Calculadora {
    // Efetua a adição de dois números reais

    public static float somar(float a, float b){
        return a+b;
    }

    // Efetua a subtração de dois números reais
    public static float subtrair(float minuendo, float subtraendo){
        return minuendo-subtraendo;
    }

    // Efetua a multiplicação de dois números reais
    public static float multiplicar(float a, float b){
        return a*b;
    }

    // Efetua a divisão de dois números reais
    public static float dividir(float dividendo, float divisor){
        return dividendo/divisor;
    }
}
